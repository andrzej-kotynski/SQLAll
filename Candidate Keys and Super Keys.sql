/*Candidate Keys and Super Keys

Candidate Key:
- single column with unique values 
or 
- a set of columns that have unique combinations of values
- a set of columns can only be a candidate key if it is irreducible i.e.
it has no unique subset of columns
- determines all other columns in a table

* Key and Non-key Attributes
- key attributes are simply columns used in at least one candidate key
- non-key attributes are any columns that do not occur as part of any candidate
key


*/